class ReportGenerator:
    """
    Generates comprehensive HTML reports from measurement results.

    Combines measurement data, statistical analysis, plot images,
    and congestion analysis into a single standalone HTML file.

    Usage:
        gen = ReportGenerator()
        gen.add_latency(latency_result.to_dict())
        gen.add_throughput(throughput_result.to_dict())
        gen.add_plot("latency.png")
        gen.generate("report.html")
    """

    def __init__(self):
        self.sections: List[str] = []
        self.target_host: str = "unknown"
        self.plot_paths: List[str] = []

    def add_latency(self, data: Dict[str, Any]) -> "ReportGenerator":
        """Add latency measurement results to the report."""
        self.target_host = data.get("host", self.target_host)
        html = f"""
    <div class="card">
        <div class="card-header">Latency Measurement</div>
        <div class="card-body">
            <div class="metrics-grid">
                <div class="metric">
                    <div class="value">{data.get('mean_rtt_ms', 0):.2f} ms</div>
                    <div class="label">Mean RTT</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('median_rtt_ms', 0):.2f} ms</div>
                    <div class="label">Median RTT</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('min_rtt_ms', 0):.2f} ms</div>
                    <div class="label">Min RTT</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('max_rtt_ms', 0):.2f} ms</div>
                    <div class="label">Max RTT</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('p95_rtt_ms', 0):.2f} ms</div>
                    <div class="label">P95 RTT</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('p99_rtt_ms', 0):.2f} ms</div>
                    <div class="label">P99 RTT</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('stddev_rtt_ms', 0):.3f} ms</div>
                    <div class="label">Std Dev</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('loss_percent', 0):.1f}%</div>
                    <div class="label">Packet Loss</div>
                </div>
            </div>
            <p style="margin-top:12px; color:#666;">
                {data.get('successful_probes', 0)} / {data.get('probe_count', 0)} probes successful
                to {data.get('resolved_ip', '')}
            </p>
        </div>
    </div>"""
        self.sections.append(html)
        return self


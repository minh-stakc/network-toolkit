    def add_congestion(self, data: Dict[str, Any]) -> "ReportGenerator":
        """Add congestion analysis results to the report."""
        severity_class = f"severity-{data.get('max_severity', 'none')}"

        events_html = ""
        for event in data.get("events", []):
            events_html += f"""
            <tr>
                <td>{event.get('signal', '')}</td>
                <td class="severity-{event.get('severity', 'mild')}">{event.get('severity', '').upper()}</td>
                <td>{event.get('start_index', 0)} - {event.get('end_index', 0)}</td>
                <td>{event.get('duration_samples', 0)}</td>
                <td>{event.get('details', '')}</td>
            </tr>"""

        recs_html = ""
        for rec in data.get("recommendations", []):
            recs_html += f'<div class="recommendation">{rec}</div>'

        html = f"""
    <div class="card">
        <div class="card-header">Congestion Analysis</div>
        <div class="card-body">
            <div class="metrics-grid">
                <div class="metric">
                    <div class="value {severity_class}">{data.get('max_severity', 'none').upper()}</div>
                    <div class="label">Max Severity</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('total_events', 0)}</div>
                    <div class="label">Total Events</div>
                </div>
                <div class="metric">
                    <div class="value">{data.get('congestion_ratio', 0)*100:.1f}%</div>
                    <div class="label">Congestion Ratio</div>
                </div>
            </div>

            {"<h3 style='margin:16px 0 8px;'>Events</h3><table><thead><tr><th>Signal</th><th>Severity</th><th>Range</th><th>Duration</th><th>Details</th></tr></thead><tbody>" + events_html + "</tbody></table>" if events_html else ""}

            {"<h3 style='margin:16px 0 8px;'>Recommendations</h3>" + recs_html if recs_html else ""}
        </div>
    </div>"""
        self.sections.append(html)
        return self

    def add_plot(self, plot_path: str, title: str = "") -> "ReportGenerator":
        """Embed a plot image (base64-encoded) into the report."""
        if not os.path.exists(plot_path):
            logger.warning("Plot file not found: %s", plot_path)
            return self

        with open(plot_path, "rb") as f:
            img_data = base64.b64encode(f.read()).decode("utf-8")

        display_title = title or Path(plot_path).stem.replace("_", " ").title()
        html = f"""
    <div class="card">
        <div class="card-header">{display_title}</div>
        <div class="card-body">
            <div class="plot-container">
                <img src="data:image/png;base64,{img_data}" alt="{display_title}">
            </div>
        </div>
    </div>"""
        self.sections.append(html)
        self.plot_paths.append(plot_path)
        return self

    def add_raw_data(self, data: Dict[str, Any], title: str = "Raw Data") -> "ReportGenerator":
        """Add a collapsible raw JSON data section."""
        json_str = json.dumps(data, indent=2, default=str)
        html = f"""
    <div class="card">
        <div class="card-header">{title}</div>
        <div class="card-body">
            <details>
                <summary style="cursor:pointer; color:#1565c0;">Click to expand</summary>
                <pre style="background:#f8f9fa; padding:16px; border-radius:4px; overflow-x:auto; margin-top:8px; font-size:12px;">{json_str}</pre>
            </details>
        </div>
    </div>"""
        self.sections.append(html)
        return self

    def generate(self, output_path: str) -> str:
        """
        Generate the final HTML report and write it to disk.

        Returns the absolute path to the generated report.
        """
        timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
        html = HTML_TEMPLATE.format(
            timestamp=timestamp,
            target_host=self.target_host,
            sections="\n".join(self.sections),
        )

        path = Path(output_path)
        path.parent.mkdir(parents=True, exist_ok=True)
        with open(path, "w", encoding="utf-8") as f:
            f.write(html)

        logger.info("HTML report generated: %s", path.resolve())
        return str(path.resolve())
